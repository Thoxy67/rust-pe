    #[test]
    fn test_write_sections() {
        let buffer = &RAW_DATA[..];
        let headerssize = get_headers_size(buffer).unwrap();
        let imagesize = get_image_size(buffer).unwrap();
        let baseptr = unsafe {
            VirtualAlloc(
                core::ptr::null_mut(),
                imagesize,
                MEM_COMMIT | MEM_RESERVE,
                PAGE_READWRITE,
            )
        };
        assert!(!baseptr.is_null(), "VirtualAlloc returned null");

        // Copy headers into allocated memory first
        unsafe {
            core::ptr::copy_nonoverlapping(buffer.as_ptr() as *const c_void, baseptr, headerssize);
        }

        let dosheader = get_dos_header(baseptr as *const c_void);
        let ntheader = get_nt_header(baseptr as *const c_void, dosheader);
        assert!(!ntheader.is_null(), "NT header signature invalid");

        write_sections(baseptr, buffer, ntheader, dosheader).unwrap();

        // Verify that the .text section was copied: check the first bytes at VirtualAddress
        // The .text section starts at VirtualAddress 0x1000 in the test PE
        let text_ptr = unsafe { (baseptr as *const u8).add(0x1000) };
        let first_byte = unsafe { *text_ptr };
        // First byte of .text in our test PE is 0x48 (REX prefix for x86_64)
        assert_eq!(first_byte, 0x48, ".text section was not correctly written");

        unsafe { VirtualFree(baseptr, 0, MEM_RELEASE) };
    }

