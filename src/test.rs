    #[test]
    fn test_ordinal_flags() {
        use crate::windows::{IMAGE_ORDINAL_FLAG32, IMAGE_ORDINAL_FLAG64};

        // PE32 ordinal flag is bit 31 (stored as usize, but only meaningful for 32-bit thunks)
        // PE32+ ordinal flag is bit 63
        // On a 64-bit host, both are usize, but IMAGE_ORDINAL_FLAG32 is 0x80000000
        assert_eq!(IMAGE_ORDINAL_FLAG32, 0x80000000usize);

        // Test ordinal detection for PE32 thunks
        let thunk32: usize = IMAGE_ORDINAL_FLAG32 | 42;
        assert!(thunk32 & IMAGE_ORDINAL_FLAG32 != 0, "Should detect ordinal import");
        assert_eq!(thunk32 & 0xFFFF, 42, "Should extract ordinal value");

        // Test ordinal detection for PE32+ thunks
        let thunk64: usize = IMAGE_ORDINAL_FLAG64 | 100;
        assert!(thunk64 & IMAGE_ORDINAL_FLAG64 != 0, "Should detect ordinal import");
        assert_eq!(thunk64 & 0xFFFF, 100, "Should extract ordinal value");

        // Non-ordinal thunks should not have the flag set
        let name_thunk: usize = 0x00001234;
        assert!(name_thunk & IMAGE_ORDINAL_FLAG64 == 0, "Name thunk should not have ordinal flag");
    }

